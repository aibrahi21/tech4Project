package primitives;

public class Long {
    public static void main(String[] args){

        // containerType label = data;
        long tester1Number = 1L;
        long tester2Number = 12L;
        // The only container type that requires an L after the number it defines is long

        long maxLong = 9223372036854775807L;
        long minLong = -9223372036854775808L;

        System.out.println(tester1Number);
        System.out.println(minLong);
        System.out.println(maxLong);

    }
}
